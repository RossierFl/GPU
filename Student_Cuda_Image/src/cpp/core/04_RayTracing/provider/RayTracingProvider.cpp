RayTracing* RayTracingProvider::create()
    {
    int dw = 16 * 60; 
    int dh = 16 * 60; 
	


    double x1 = 0;
    double x2 = dw;
    double y1 = 0;
    double y2 = dh;
    double z1 = 10;
    double z2 = x2*2;
    int nbrBalls = 50;
    float rayonMax = x2/10;

    srand48(time(NULL));


    float dt = 0.01f;

    return new RayTracing(dw, dh, dt, x1, y1, x2, y2, z1,z2,rayonMax, nbrBalls);
    }

Image* RayTracingProvider::createGL(void)
    {
    ColorRGB_01* ptrColorTitre=new ColorRGB_01(1,1,1);

    return new Image(create(),ptrColorTitre);
    }

