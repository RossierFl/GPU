Fractal* FractalProvider::create() {
	int dw = 16; // =32*30=960
	int dh = 16; // =32*30=960
	float dt = 20;
	int n = 12;
	double x1 = -1.3968;
	double y1 = -0.03362;
	double x2 = -1.3578;
	double y2 = 0.0013973;

	return new Fractal(dw, dh, dt, n, x1, y1, x2, y2);
}

ImageFonctionel* FractalProvider::createGL() {
	return new ImageFonctionel(create());
}

