Fractal* FractalProvider::createMOO() {
	int dw = 16 * 60; // =32*30=960
	int dh = 16 * 60; // =32*30=960
	float dt = 1;
	int n = 12;
	int x1 = -2.1;
	int y1 = 0.8;
	int x2 = -1.3;
	int y2 = 1.3;

	return new Fractal(dw, dh, dt, n, x1, y1, x2, y2);
}

ImageFonctionel* FractalProvider::createGL(void) {
	return new ImageFonctionel(createMOO());
}

