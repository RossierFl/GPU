namespace cpu
    {

    /**
     * F Pour Float
     */
    class IntervalF
	{

	    /*--------------------------------------*\
	    |*		Constructor	      		*|
	     \*-------------------------------------*/

	public:

	    IntervalF(float a, float b)
		{
		this->a = a;
		this->b = b;
		}

	    /*--------------------------------------*\
	    |*		Methode	      		*|
	     \*-------------------------------------*/

	public:

	    float getA(void)
		{
		return this->a;
		}

	    float getB(void)
		{
		return this->b;
		}

	    /*--------------------------------------*\
	   |*		Attribut	      		*|
	     \*-------------------------------------*/

	public:

	    float a;
	    float b;
	};

    }
